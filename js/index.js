
function toggleBookmark() 
{
    let bookmark = document.getElementById("bookmark");
    if (bookmark.src.match("./icons/home/bookmark-white.png")) 
    {
      bookmark.src = "./icons/home/bookmark.png";
    } 
    else 
    {
      bookmark.src = "./icons/home/bookmark-white.png";
    }
}
